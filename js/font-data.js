/**
 * Find the best source font map for scaling to the given target dimensions.
 * Prefers an exact match, then the closest map by aspect-ratio similarity.
 */
function bestFontSource(dstCols, dstRows) {
  const key = `${dstCols}x${dstRows}`;
  if (FONT_MAPS[key]) return FONT_MAPS[key];

  // Pick the map whose aspect ratio is closest, preferring larger sources
  const targetAR = dstCols / dstRows;
  let best = FONT_MAPS['5x7'];  // fallback
  let bestScore = Infinity;

  for (const m of Object.values(FONT_MAPS)) {
    const ar = m.cols / m.rows;
    const arDiff = Math.abs(ar - targetAR);
    const sizeDiff = Math.abs(m.cols - dstCols) + Math.abs(m.rows - dstRows);
    const score = arDiff * 10 + sizeDiff;
    if (score < bestScore) {
      bestScore = score;
      best = m;
    }
  }
  return best;
}
